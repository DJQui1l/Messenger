class Equipment {
  constructor(name, type) {
    this.name = name
    this.type = type
    this.hp = 0
    this.atk = 0
    this.def = 0
  }
}


let broadsword = new Equipment('Broadsword','hand')
let shield = new Equipment('Kite Shield', 'hand')
let chainmail = new Equipment('Chain Mail','armor')
let helm = new Equipment('Helmet', 'helmet')
let access = new Equipment('Hermes Shoes', 'accessory')
